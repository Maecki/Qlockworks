<h1>&Uuml;ber Uns</h1>

<h2>Wieso, weshalb, warum?</h2>
<img src="style/bunka_bird.png" width=130 style="float:left;margin:5px 20px;" />
Wieso "Bunka Birds"?<br />
Der Name setzt sich aus den beiden W&ouml;rtern "Bunka" und "Birds" zusammen. <b>"Bunka"</b> bzw. ein Bunker ist ein gesch&uuml;tzes Bauwerk, das Insassen oder die Umgebung vor Gef&auml;hrdungen sch&uuml;tzen Soll.
In unserem Fall haben wir als Proberaum einen kleinen Raum in einem Bunker gemietet und k&ouml;nnen so die Aussenwelt vor unserem L&auml,rm sch&uuml;tzen.<br />
<b>"Birds"</b> zu deutsch "Vogel" wurde gew&auml;hlt, weil unser S&auml;nger, Seve, an jede Passende Liedstelle "Birds" einf&uuml;gt, wir seit Anfangszeiten einen kleinen Pinguin als Maskotchen im Proberaum haben und oder weil wir alle einen Vogel haben und zuf&auml;llig der Anfangsbuchstabe "B" zu unserem "Bunker" passt.
